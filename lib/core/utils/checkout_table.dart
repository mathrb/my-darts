/// Lookup config for each out strategy: (table, minScore, maxScore).
({Map<int, String> table, int min, int max}) _configFor(String outStrategy) =>
    switch (outStrategy) {
      'straight' => (
        table: kStraightOutCheckoutTable,
        min: 1,
        max: 180,
      ),
      'master' => (
        table: kMasterOutCheckoutTable,
        min: 2,
        max: 180,
      ),
      _ => (table: kCheckoutTable, min: 2, max: 170),
    };

/// Returns a checkout suggestion appropriate for the given [outStrategy].
///
/// - `'double'`: final dart must be a double or DB (range 2–170)
/// - `'straight'`: any segment can finish (range 1–180)
/// - `'master'`: final dart must be a double or triple (range 2–180)
String? checkoutSuggestionForStrategy(int score, String outStrategy) =>
    _configFor(outStrategy).table[score];

/// Maximum checkout score for the given [outStrategy].
int maxCheckoutScore(String outStrategy) => _configFor(outStrategy).max;

/// Minimum checkout score for the given [outStrategy].
int minCheckoutScore(String outStrategy) => _configFor(outStrategy).min;
